//C++ program to print characters in a reverse way.

#include <iostream>

using namespace std;

int main()

{
    char x, y, z;

    cout<<endl<<"Enter three characters: "<<endl;

    cin>>x;

    cin>>y;

    cin>>z;

    cout<<"The reverse of "<<x<<y<<z<<" is "<<z<<y<<x<<endl;



    return 0;
}
